CREATE TABLE DIM_ROAST_TYPE(
	DIM_ROAST_TYPE_ID INT PRIMARY KEY,
	ROAST_TYPE VARCHAR(20) NOT NULL
)

INSERT INTO DIM_ROAST_TYPE
SELECT DISTINCT
	ROAST_TYPE_ID AS DIM_ROAST_TYPE_ID,
	A.ROAST_TYPE
FROM COFFEE_SALES.DBO.ROAST_TYPE A
WHERE NOT EXISTS(
	SELECT 1
	FROM DIM_ROAST_TYPE
	WHERE A.ROAST_TYPE_ID = DIM_ROAST_TYPE_ID
)