CREATE TABLE DIM_COFFEE_TYPE(
	DIM_COFFEE_TYPE_ID INT PRIMARY KEY,
	COFFEE_TYPE VARCHAR(20) NOT NULL
)

INSERT INTO DIM_COFFEE_TYPE
SELECT DISTINCT
	COFFEE_TYPE_ID AS DIM_COFFEE_TYPE_ID,
	A.COFFEE_TYPE
FROM COFFEE_SALES.DBO.COFFEE_TYPE A
WHERE NOT EXISTS(
	SELECT 1
	FROM DIM_COFFEE_TYPE
	WHERE A.COFFEE_TYPE_ID = DIM_COFFEE_TYPE_ID
)