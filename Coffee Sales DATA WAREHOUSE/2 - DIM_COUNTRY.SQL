CREATE TABLE DIM_COUNTRY(
	DIM_COUNTRY_ID INT PRIMARY KEY,
	COUNTRY VARCHAR(20) NOT NULL
)

INSERT INTO DIM_COUNTRY
SELECT DISTINCT
	COUNTRY_ID AS DIM_COUNTRY_ID,
	COUNTRY
FROM COFFEE_SALES.DBO.COUNTRY A
WHERE NOT EXISTS(
    SELECT 1 
    FROM DIM_COUNTRY 
    WHERE A.COUNTRY_ID = COUNTRY_ID
)