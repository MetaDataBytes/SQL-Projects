CREATE TABLE COFFEE_TYPE(
	COFFEE_TYPE_ID INT IDENTITY(0, 1) PRIMARY KEY,
	COFFEE_TYPE VARCHAR(20) NOT NULL
)

INSERT INTO COFFEE_TYPE
SELECT DISTINCT 
	CASE 
		WHEN UPPER(COFFEE_TYPE) = 'ARA' THEN 'ARABICA'
		WHEN UPPER(COFFEE_TYPE) = 'EXC' THEN 'EXCELSA'
		WHEN UPPER(COFFEE_TYPE) = 'LIB' THEN 'LIBERICA'
		WHEN UPPER(COFFEE_TYPE) = 'ROB' THEN 'ROBUSTA'
		ELSE UPPER(COFFEE_TYPE)
	END AS COFFEE_TYPE
FROM STAGING_TABLE
ORDER BY COFFEE_TYPE