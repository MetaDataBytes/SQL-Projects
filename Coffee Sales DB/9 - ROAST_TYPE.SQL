CREATE TABLE ROAST_TYPE(
	ROAST_TYPE_ID INT IDENTITY(0, 1) PRIMARY KEY,
	ROAST_TYPE VARCHAR(20) NOT NULL
)

INSERT INTO ROAST_TYPE
SELECT DISTINCT
	CASE
		WHEN UPPER(ROAST_TYPE) = 'D' THEN 'DARK'
		WHEN UPPER(ROAST_TYPE) = 'M' THEN 'MEDIUM'
		WHEN UPPER(ROAST_TYPE) = 'L' THEN 'LIGHT'
		ELSE UPPER(ROAST_TYPE)
	END AS ROAST_TYPE
FROM STAGING_TABLE
ORDER BY ROAST_TYPE